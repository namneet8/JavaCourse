// In this exercise, we will explore-
// System.out.print()
// System.out.println()
// \n -> new line character

public class PrintingOutput_1 {
    public static void main (String args[]){
        System.out.print("My name is "); //prints the String and the next line will be executed in same line
        System.out.println("Namneet."); // next line will be executed in a new line
        System.out.print("I am 21 years old.\n"); // \n character takes cursor to next line 
        System.out.print("I am learning Java.");
    }
    
}
