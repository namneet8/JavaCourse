// Relation Operators (Return true or false)
// ==, !=, <, >, <=, >=
// Logical Operators- && (Logical AND), || (Logical OR), !(Logical NOT)

public class RelationalLogicalOperators_2 {
    public static void main (String args[]){
        System.out.println( (10 <= 12) && (10 > 4) );
        System.out.println( (1 == 2) || (10 > 4) );
        System.out.println( !(1==2) );
    }
    
}
